<h2>Duties and responsibilities of the job</h2>
<p>A standard Software Developer job description should include, but not be limited to:</p>
<ol>
<li>Researching, designing, implementing and managing software programs</li>
<li>Testing and evaluating new programs</li>
<li>Identifying areas for modification in existing programs and subsequently developing these modifications</li>
<li>Writing and implementing efficient code</li>
<li>Determining operational practicality</li>
<li>Developing quality assurance procedures</li>
<li>Deploying software tools, processes and metrics</li>
<li>Maintaining and upgrading existing systems</li>
</ol>
<h2>&nbsp;Job qualifications and requirements</h2>
<p>A degree and a technical background should be required to gain a Software Developer role. Degrees in the following subjects should be highlighted as particularly advantageous:</p>
<ul>
<li>Computer Science</li>
<li>Computer Software Engineering</li>
<li>Software Programming and Development</li>
<li>Engineering</li>
<li>Mathematics</li>
<li>Electronics</li>
<li>Physics</li>
</ul>
<p>As well as formal qualifications, a Software Developer job description should highlight the following qualities:</p>
<ul>
<li>Knowledge and interest in computer systems and the latest technologies</li>
<li>The ability to learn new technologies quickly</li>
<li>An analytical mind</li>
<li>The ability to communicate complex procedures to other colleagues</li>
<li>Commercial and business awareness</li>
<li>Good communication skills &ndash; both written and verbal</li>
<li>Attention to detail and desire to probe further into data</li>
</ul>
