<h2>Duties and responsibilities of the job</h2>
<p>A Web Developer must have experience in the planning and the delivery of web applications across multiple platforms. A typical Web Developer job description should cover:</p>
<ol>
<li>Writing efficient code</li>
<li>Creating websites/a website using standard HTML/CSS practices</li>
<li>Working closely with web designers and programmers to produce the website</li>
<li>Constant communication with other colleagues in the business to develop and deploy their content &ndash; and ensuring there is a clear establishment of what can be created within what timeframe</li>
<li>Researching different software programs, maintaining software documentation</li>
<li>Implementing contingency plans in case the website goes down</li>
<li>Maintaining and expanding/enhancing the website once built</li>
<li>Managing a team might also be part of the job role</li>
</ol>
<h2>Job qualifications and requirements</h2>
<p>Although there are no formal educational requirements to become a Web Developer, experience in the field will be expected. Knowledge in the following is a requirement:</p>
<ul>
<li>HTML/XHTML, CSS, JavaScript</li>
<li>Server architecture</li>
<li>Experience with server-side frameworks such as python, ruby, php, Java, ASP, ASP.NET</li>
<li>Experience with database systems such as SQL and Oracle</li>
</ul>
<p>A Bachelor&rsquo;s degree in the following subjects should be included:</p>
<ul>
<li>Computer Science</li>
<li>Computing</li>
</ul>
<p>Aside from these skills, there are a number of qualities to be highlighted in a Web Developer job description to ensure the most suitable candidates apply:</p>
<ul>
<li>Thorough functional knowledge and coding experience</li>
<li>Basic knowledge of Search Engine Optimisation</li>
<li>Ability to multitask with strict time constraints, budgets and business goals</li>
<li>Strong communication skills</li>
<li>Management experience or examples that show ability to manage a team, should this role involve team management</li>
</ul>
